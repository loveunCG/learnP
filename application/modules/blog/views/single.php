				<div class="blog-post-description">
					<p>I’ve seen some students plan out their moves like a game of chess. They are several moves ahead and playing the long game. Study only suffers a little, but the payoff is much bigger in the grand scheme of things. The pros outweigh the cons by a wide margin. A big picture attitude is hard to fake. You don’t fall into it by accident.</p>
					<p> In other words, current priorities have to tie in with future priorities, otherwise you have a mismatch.</p>
					<p> Without a match, you get into trouble when you reach the next point on your roadmap. You realise too late that your priorities don’t reflect the person you wanted to be after graduation.</p>
					<h3 class="blog-quote">Life is a series of natural and spontaneous changes. Don't resist them - that only creates sorrow. Let reality be reality.</h3>
					<p>One way to begin shaping your narrative and honing your professional persona could be to separate the extra-curricular and the professional.</p>
					<h2>The journey of a thousand miles begins with one step.</h2>
					<p>I tend to call anything outside the degree as ‘extra-curricular’. This could be a mistake.</p>
					<p>A survey asked recruiters what they are looking for in graduates. Recruiters (and the survey) saw ‘professional experience’ as separate to ‘extra-curricular activities’.</p>
					<p>That’s not to say you can’t get professional experience from clubs and societies. But it does highlight the need to tell your story effectively. Especially when activities aren’t always considered so important by employers.</p>
					<p>To impress recruiters, emphasise the business qualities of your experiences, wherever they occurred. That means highlighting your achievements and framing what you’ve done through examples and a narrative flow. You’ll impress more when you frame your achievements in the context of professional development.</p>
					<h3 class="blog-quote">Kindness in words creates confidence. Kindness in thinking creates
						profoundness. Kindness in giving creates love.</h3>
